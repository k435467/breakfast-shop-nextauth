import {
  Container,
  Button,
  Typography,
  Accordion,
  AccordionSummary,
  AccordionDetails,
  List,
  ListItem,
  ListItemText,
  ListItemSecondaryAction,
  Box,
} from "@mui/material";
import ExpandMoreIcon from "@mui/icons-material/ExpandMore";
import type { GetServerSideProps, NextPage } from "next";
import Head from "next/head";
import { signIn, signOut, useSession } from "next-auth/client";
import IMenu from "../lib/model/IMenu";
import { useState } from "react";
import AddCategoryModal from "../component/AddCategoryModal";

const Home: NextPage<{ menu: IMenu[] }> = ({ menu }) => {
  const [session, loading] = useSession();

  const [openAddCategoryModal, setOpenAddCategoryModal] = useState(false);
  const handleOpenAddCategoryModal = () => setOpenAddCategoryModal(true);
  const handleCloseAddCategoryModal = () => setOpenAddCategoryModal(false);

  return (
    <>
      <Head>
        <title>Home Page</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Container>
        {!session && (
          <>
            Not signed in <br />
            <Button variant="contained" onClick={() => signIn()}>
              Sign in
            </Button>
          </>
        )}
        {session && (
          <>
            Signed in as {session.user?.email} <br />
            <Button variant="outlined" color="error" onClick={() => signOut()}>
              Sign out
            </Button>
          </>
        )}
        <Box display="flex" justifyContent="end">
          <Button
            variant="contained"
            color="success"
            onClick={handleOpenAddCategoryModal}
          >
            Add Category
          </Button>
          <Button variant="contained" color="success">
            Add Item
          </Button>
        </Box>
        <AddCategoryModal
          open={openAddCategoryModal}
          handleClose={handleCloseAddCategoryModal}
        />
        <div>
          {menu.map((category) => {
            return (
              <Accordion key={category.id}>
                <AccordionSummary
                  expandIcon={<ExpandMoreIcon />}
                  aria-controls={`panel${category.id}a-content`}
                  id={`panel${category.id}a-header`}
                >
                  <Typography variant="h6">{category.title}</Typography>
                </AccordionSummary>
                <AccordionDetails>
                  <List>
                    {category.menuItems.map((item) => {
                      return (
                        <ListItem key={item.id}>
                          <ListItemText
                            primary={item.title}
                            style={{ wordBreak: "keep-all" }}
                          ></ListItemText>
                          <ListItemSecondaryAction>
                            <Typography>{item.price}</Typography>
                          </ListItemSecondaryAction>
                        </ListItem>
                      );
                    })}
                  </List>
                </AccordionDetails>
              </Accordion>
            );
          })}
        </div>
      </Container>
    </>
  );
};

export const getServerSideProps: GetServerSideProps = async (context) => {
  const res = await fetch("http://localhost:8080/menucategory");
  const data: IMenu[] = await res.json();

  return {
    props: {
      menu: data,
    },
  };
};

export default Home;
